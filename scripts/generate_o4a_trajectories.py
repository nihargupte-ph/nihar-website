"""Precompute pyseobnr two-body trajectories + h+ for the O4a catalog page.

For each event in ``static/data/posteriors_ecc.h5`` this script:

  1. Picks the maximum-a-posteriori sample (argmax of log_likelihood +
     log_prior). MAP sits in the densest region of the posterior, which
     keeps the chosen point away from tail outliers that often land
     outside SEOBNRv5EHM's domain (high eccentricity, short inspiral,
     starting separation below the model floor).
  2. Calls ``blog.waveforms.generate_eccentric_bbh_waveform`` to run
     ``SEOBNRv5EHM`` with that sample's parameters.
  3. Resamples the **full inspiral** (from f22_start to merger) onto a
     uniform time grid so the JS playback can advance through index space
     at a constant rate. The chirp toward merger remains visually obvious
     because the orbital frequency itself is increasing in real time.
  4. Resamples h+ onto the same uniform grid so the orbit and waveform
     stay in lockstep when the frontend plays them back.
  5. Writes everything to ``static/data/trajectories_ecc.h5`` as one HDF5
     group per event.

The output file is a sidecar to ``posteriors_ecc.h5`` and is regenerated by
re-running this script — safe after a posteriors swap-in from Zenodo.

Run with::

    python scripts/generate_o4a_trajectories.py
"""
from __future__ import annotations

import os
import sys
from pathlib import Path

import h5py
import numpy as np
from pesummary.gw.conversions.nrutils import (
    bbh_final_mass_average,
    bbh_final_spin_average_non_precessing,
)
from scipy.interpolate import CubicSpline

REPO = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(REPO))
os.environ.setdefault('DJANGO_SETTINGS_MODULE', 'nihar_website.settings')

import django  # noqa: E402

django.setup()

from blog.waveforms import generate_eccentric_bbh_waveform  # noqa: E402

POSTERIORS = REPO / 'static' / 'data' / 'posteriors_ecc.h5'
OUTPUT = REPO / 'static' / 'data' / 'trajectories_ecc.h5'

ML_COLS = [
    'mass_1',
    'mass_2',
    'chi_1',
    'chi_2',
    'eccentricity',
    'relativistic_anomaly',
    'phase',
    'theta_jn',
    'luminosity_distance',
]

# Each event's full inspiral is resampled onto a uniform time grid with
# enough points per orbit that the JS's straight-line renderer looks
# smooth. ``TRAJ_PTS_PER_ORBIT`` targets ~30 samples per orbit — beyond
# that the orbit is visually indistinguishable from a smooth curve —
# and ``TRAJ_POINTS_MIN`` / ``TRAJ_POINTS_MAX`` clamp the total to
# keep short events reasonable and long events from blowing up the
# payload.
TRAJ_PTS_PER_ORBIT = 30
TRAJ_POINTS_MIN = 800
TRAJ_POINTS_MAX = 3000

# Number of points for the post-merger ringdown hp segment. The pyseobnr
# polarization continues past merger into the ringdown via attached
# quasinormal modes; we resample that segment uniformly so the JS playback
# advances at a constant rate.
RING_POINTS = 200

# SEOBNRv5EHM is unstable above ~e=0.5; clip ML eccentricities defensively
# so the precompute doesn't crash on tail samples.
ECC_CEIL = 0.5

# When the MAP sample lands outside SEOBNRv5EHM's domain, fall through to
# the next-most-probable sample. 200 fallback candidates is plenty for
# well-converged events; pathological events that need more than this are
# likely entirely in the unphysical corner and should fall back to the
# no-waveform placeholder anyway.
MAX_TRIES = 200

# Boundary between the "quasicircular" and "eccentric" bins of the
# population prior the catalog page lets the user reweight against. Each
# event gets up to two precomputed trajectories — one MAP sample drawn
# from below and one from above this boundary — so the JS can swap which
# is shown when the user picks a different prior odds.
QC_BOUNDARY = 0.05


def _bin_order(grp, mask):
    """Return indices within ``mask`` sorted by descending posterior.

    The first index is the bin's MAP. Subsequent indices are progressively
    lower posterior samples, used as fallback candidates when pyseobnr
    rejects the MAP for being outside its domain (high eccentricity,
    dynamics shorter than 200 M, starting separation below the model
    floor, …). Within a bin the eccentricity-prior reweighting factor is
    constant, so sorting by the original log_likelihood + log_prior gives
    the correct order under any of the catalog page's prior options.
    """
    bin_idx = np.where(mask)[0]
    if len(bin_idx) == 0:
        return bin_idx
    ll = grp['log_likelihood'][bin_idx]
    lp = grp['log_prior'][bin_idx]
    return bin_idx[np.argsort(-(ll + lp))]


def _params_at(grp, idx):
    return {col: float(grp[col][idx]) for col in ML_COLS}


def main():
    if not POSTERIORS.exists():
        raise SystemExit(f"posteriors file not found: {POSTERIORS}")

    if OUTPUT.exists():
        OUTPUT.unlink()

    written = 0
    with h5py.File(POSTERIORS, 'r') as f_in, h5py.File(OUTPUT, 'w') as f_out:
        f_out.attrs['source'] = str(POSTERIORS.name)
        f_out.attrs['traj_pts_per_orbit'] = TRAJ_PTS_PER_ORBIT
        f_out.attrs['traj_points_min'] = TRAJ_POINTS_MIN
        f_out.attrs['traj_points_max'] = TRAJ_POINTS_MAX

        # The unified catalog groups events under O1/O2/O3/O4a; iterate
        # all runs and write trajectories flat at the top level (event
        # names are unique across runs). Old layouts (events at the file
        # root) still work via the fallback branch.
        if any(run in f_in for run in ('O1', 'O2', 'O3', 'O4a')):
            event_groups = []
            for run in ('O1', 'O2', 'O3', 'O4a'):
                if run not in f_in:
                    continue
                run_grp = f_in[run]
                for name in sorted(run_grp.keys()):
                    event_groups.append((name, run_grp[name]))
        else:
            event_groups = [(name, f_in[name]) for name in sorted(f_in.keys())]

        for name, grp_in in event_groups:
            e_all = grp_in['eccentricity'][...]
            qc_mask = e_all < QC_BOUNDARY
            ecc_mask = ~qc_mask

            # Each event yields up to two trajectories — one MAP sample
            # from the quasicircular bin and one from the eccentric bin —
            # so the JS can swap which one is shown when the user picks
            # a different population prior on eccentricity. Bins with no
            # support in the posterior are silently skipped.
            bin_orders = (
                ('qc', _bin_order(grp_in, qc_mask)),
                ('ecc', _bin_order(grp_in, ecc_mask)),
            )

            event_grp_out = None
            bins_done = 0
            for bin_name, order in bin_orders:
                if len(order) == 0:
                    continue

                # Walk down the bin in MAP-then-fallback order until
                # pyseobnr accepts a sample. Cap the search at MAX_TRIES
                # so a pathological bin doesn't burn unbounded time.
                result = None
                ml = None
                idx = -1
                last_err = None
                for cand in order[:MAX_TRIES]:
                    cand = int(cand)
                    params = _params_at(grp_in, cand)
                    try:
                        result = generate_eccentric_bbh_waveform(
                            m1=params['mass_1'],
                            m2=params['mass_2'],
                            spin1z=params['chi_1'],
                            spin2z=params['chi_2'],
                            eccentricity=min(params['eccentricity'], ECC_CEIL),
                            rel_anomaly_rad=params['relativistic_anomaly'],
                            inclination=params['theta_jn'],
                            distance=params['luminosity_distance'],
                            # Start at 10 Hz (the posteriors define ecc
                            # there) but only ship from 20 Hz.
                            f22_start=10.0,
                            f22_display=20.0,
                            # Restrict the Nyquist check to ell=2 modes;
                            # some events have a (4,4) ringdown frequency
                            # above Nyquist at the default deltaT.
                            lmax_nyquist=2,
                        )
                        ml = params
                        idx = cand
                        break
                    except Exception as e:
                        last_err = e
                        continue

                if result is None:
                    print(f"  SKIP {name}/{bin_name}: no usable sample in "
                          f"top {MAX_TRIES} (last error: {last_err})")
                    continue

                traj = result['trajectory']
                meta = result['meta']
                t_traj = traj['t']
                r_traj = traj['r']
                phi_traj = traj['phi']
                t_pol = result['polarizations']['t']
                hp_pol = result['polarizations']['hp']
                hc_pol = result['polarizations']['hc']

                # Resample the full inspiral onto a uniform time grid
                # dense enough that the JS's straight-line rendering
                # doesn't turn the orbit into a visible polygon. The
                # target is ~30 samples per orbit, clamped between
                # TRAJ_POINTS_MIN and TRAJ_POINTS_MAX.
                n_orbits = max(1.0,
                               abs(phi_traj[-1] - phi_traj[0]) / (2.0 * np.pi))
                traj_points = int(min(TRAJ_POINTS_MAX,
                                      max(TRAJ_POINTS_MIN,
                                          round(TRAJ_PTS_PER_ORBIT * n_orbits))))
                # The dynamics array ends at merger (t_traj[-1] =
                # merger_time, set in waveforms.py via
                # t_dyn - t_dyn[-1] + t_peak), so playback naturally
                # stops at merger; the EOB step sizing was adaptive but
                # the uniform grid hands the JS a constant index <->
                # time mapping.
                t_uniform = np.linspace(t_traj[0], t_traj[-1], traj_points)

                # Interpolate the *native* EOB polar dynamics r(t) and
                # phi(t) with cubic splines and reconstruct Cartesian
                # positions on the uniform grid. Linearly interpolating
                # x/y directly draws chords across the orbit and shows
                # up as visible polygons on eccentric events; cubic
                # interpolation in (r, phi) preserves curvature because
                # phi is monotonic and r varies smoothly.
                r_spline = CubicSpline(t_traj, r_traj)
                phi_spline = CubicSpline(t_traj, phi_traj)
                r_u = r_spline(t_uniform)
                phi_u = phi_spline(t_uniform)
                cos_phi = np.cos(phi_u)
                sin_phi = np.sin(phi_u)
                m1_frac = meta['m1_frac']
                m2_frac = meta['m2_frac']
                x1_u =  m2_frac * r_u * cos_phi
                y1_u =  m2_frac * r_u * sin_phi
                x2_u = -m1_frac * r_u * cos_phi
                y2_u = -m1_frac * r_u * sin_phi

                # Same cubic treatment for the strain — t_pol is already
                # uniformly sampled by pyseobnr at deltaT, but cubic keeps
                # the orbit/strain animation phase-locked at the sub-sample
                # level that linear np.interp would smear.
                hp_spline = CubicSpline(t_pol, hp_pol)
                hc_spline = CubicSpline(t_pol, hc_pol)
                hp_u = hp_spline(t_uniform)
                hc_u = hc_spline(t_uniform)

                # Ringdown polarizations: the pyseobnr output extends past
                # merger into the ringdown via attached QNM modes. Slice
                # from merger (= t_traj[-1]) to the end of the polarization
                # and resample onto a uniform grid. If pyseobnr produced
                # no post-merger samples for some reason, ship one-point
                # datasets so the JS has something to fall back on.
                merger_time = float(t_traj[-1])
                if t_pol[-1] > merger_time:
                    t_ring_uniform = np.linspace(merger_time, float(t_pol[-1]),
                                                 RING_POINTS)
                    hp_ring_u = hp_spline(t_ring_uniform)
                    hc_ring_u = hc_spline(t_ring_uniform)
                else:
                    t_ring_uniform = np.array([merger_time], dtype=float)
                    hp_ring_u = np.array([float(hp_u[-1])], dtype=float)
                    hc_ring_u = np.array([float(hc_u[-1])], dtype=float)

                if event_grp_out is None:
                    event_grp_out = f_out.create_group(name)
                bin_grp = event_grp_out.create_group(bin_name)
                for col, arr in [
                    ('t', t_uniform),
                    ('x1', x1_u),
                    ('y1', y1_u),
                    ('x2', x2_u),
                    ('y2', y2_u),
                    ('hp', hp_u),
                    ('hc', hc_u),
                    ('t_ringdown', t_ring_uniform),
                    ('hp_ringdown', hp_ring_u),
                    ('hc_ringdown', hc_ring_u),
                ]:
                    bin_grp.create_dataset(col, data=arr, compression='gzip')
                bin_grp.attrs['ml_idx'] = idx
                bin_grp.attrs['duration_s'] = float(t_uniform[-1] - t_uniform[0])
                bin_grp.attrs['ringdown_duration_s'] = float(
                    t_ring_uniform[-1] - t_ring_uniform[0]
                )
                for k, v in ml.items():
                    bin_grp.attrs[k] = v

                # NR-fit remnant mass and spin (pesummary averages over
                # multiple NR surrogate fits). These replace the naive
                # m1+m2 / hardcoded chi=0.69 that the JS used before.
                _m1 = ml['mass_1']
                _m2 = ml['mass_2']
                _s1 = ml['chi_1']
                _s2 = ml['chi_2']
                _mf = bbh_final_mass_average(_m1, _m2, _s1, _s2)
                _af = bbh_final_spin_average_non_precessing(
                    _m1, _m2, _s1, _s2
                )
                bin_grp.attrs['final_mass'] = float(
                    _mf.item() if hasattr(_mf, 'item') else _mf
                )
                bin_grp.attrs['final_spin'] = float(
                    _af.item() if hasattr(_af, 'item') else _af
                )

                print(f"  OK   {name}/{bin_name}  ({len(bin_grp['t'])} insp + "
                      f"{len(bin_grp['hp_ringdown'])} ring pts, "
                      f"{t_uniform[-1] - t_uniform[0]:.3f}s + "
                      f"{t_ring_uniform[-1] - t_ring_uniform[0]:.4f}s, "
                      f"Mf={bin_grp.attrs['final_mass']:.2f} "
                      f"af={bin_grp.attrs['final_spin']:.4f})")
                bins_done += 1

            if bins_done == 0:
                print(f"  SKIP {name}: no usable sample in either bin")
            else:
                written += bins_done

    print(f"Wrote {written} bin trajectories to {OUTPUT}")


if __name__ == '__main__':
    main()
